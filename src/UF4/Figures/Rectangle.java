package UF4.Figures;

public class Rectangle {
    public int base;
    public int alçada;
    public String color;

    public Rectangle() {
    }

    public Rectangle(int b, int a) {
        b=base;
        a=alçada;
    }

    public Rectangle(int b, int a, String c) {
        b=base;
        a=alçada;
        c=color;
    }

}