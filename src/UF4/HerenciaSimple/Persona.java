package UF4.HerenciaSimple;

public class Persona {
    public int getEdad() {
        return edad;
    }

    public int getId() {
        return id;
    }

    public String getNom() {
        return Nom;
    }

    public String getCognom() {
        return Cognom;
    }

    public String getAdreca() {
        return adreca;
    }

    private int edad;
    private int id;
    private String Nom;
    private String Cognom;
    private String adreca;
}
